public class InsJornadaDTO {
	
	private Integer codJornada;
	private String jornadaDescripcion;
	private Integer codInstitucion;
	private Integer codRegAniLec;
	
	
	public Integer getCodJornada() {
		return codJornada;
	}
	public void setCodJornada(Integer codJornada) {
		this.codJornada = codJornada;
	}
	public String getJornadaDescripcion() {
		return jornadaDescripcion;
	}
	public void setJornadaDescripcion(String jornadaDescripcion) {
		this.jornadaDescripcion = jornadaDescripcion;
	}
	public Integer getCodInstitucion() {
		return codInstitucion;
	}
	public void setCodInstitucion(Integer codInstitucion) {
		this.codInstitucion = codInstitucion;
	}
	public Integer getCodRegAniLec() {
		return codRegAniLec;
	}
	public void setCodRegAniLec(Integer codRegAniLec) {
		this.codRegAniLec = codRegAniLec;
	}
	

   

   
}
